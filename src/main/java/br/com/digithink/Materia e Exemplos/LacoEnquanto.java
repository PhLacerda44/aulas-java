package br.com.digithink;

public class LacoEnquanto {
    
    public static void main(String[] args) {
        
        //VARIÁVEL DE ÍNDICE
        int indice = 1;

        //Laço de repetição
        while (indice <= 10) {
            System.out.println(indice);
            indice++;
        }

        System.out.println("Continua..");
    }
    
}
